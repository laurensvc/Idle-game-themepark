export const getRideDefinition = (id: string): RideDefinition | undefined => RIDE_DEFINITIONS.find((r) => r.id === id);

/** Calculate level-up cost: baseLevelUpCost * currentLevel^1.5 */
export const getLevelUpCost = (def: RideDefinition, currentLevel: number): number =>
  Math.floor(def.baseLevelUpCost * Math.pow(currentLevel, 1.5));

/** Capacity multiplier from ride level: +10% per level above 1 */
export const getLevelCapacityMultiplier = (level: number): number => 1 + (level - 1) * 0.1;

/** Income multiplier from ride level: +8% per level above 1 */
export const getLevelIncomeMultiplier = (level: number): number => 1 + (level - 1) * 0.08;
